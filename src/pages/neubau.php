                <p>
                    Für dieses Jahr ist in <strong>Trier-West</strong> ein weiteres Großprojekt geplant: 
                    Neben dem weiter laufenden Bau der neuen Verbindungsstraße zwischen der Hornstraße und der Lokrichthalle wird der <strong>Neubau der 
                    Eisenbahnüberführung</strong> zwischen der Luxemburger und der Eurener Straße beginnen. In das Projekt fließt ein zweistelliger Millionenbetrag.
                </p>
                <h2 id="1">Schwerlastverkehr bereits eingeschränkt</h2>
                <p> 
                    Die Eisenbahnüberführung wurde bei der letzten Bauwerksprüfung mit der <strong>Note 3,5 bewertet</strong>, 
                    informiert die Stadt Trier. Ab Note 4 müsste die Brücke aus Sicherheitsgründen für den Verkehr 
                    gesperrt werden. Die <strong>schlechte Bewertung</strong> führte dazu, dass bereits jetzt <strong>Schwerlastverkehr</strong> ab 7,5 Tonnen die Brücke nicht mehr befahren darf. 
                    Davon betroffen sind auch die Stadtbuslinien 1 und 81. Ein Neubau der für die Verkehrsanbindung von 
                    Trier-West unverzichtbaren Überführung sei daher dringend notwendig.
                </p>
                <h2 id="2">Wie sehen die Pläne für den Neubau aus?</h2>
                <p> 
                    Neben dem Brückenbauwerk aus Stahlbeton werden auch die Widerlager und die drei Auffahrtsrampen 
                    inklusive Stützmauern aus Richtung Eurener Straße, Hornstraße und Luxemburger Straße erneuert. 
                    Dabei muss berücksichtigt werden, dass die <strong>neue Brücke</strong> 70 Zentimeter über dem bisherigen Niveau liegt, 
                    um den Sicherheitsabstand zu den Oberleitungen der Bahnlinie einzuhalten. Der Querschnitt der Fahrbahn gliedert 
                    sich in zwei 3,25 Meter breite Fahrstreifen mit beidseitigem Angebotsstreifen für Radfahrerinnen und Radfahrer 
                    (1,85 Meter) und 2,50 Meter breiten Gehwegen. Auf den westlichen Auffahrten sind begrünte Verkehrsinseln und Querungsstellen vorgesehen.
                </p>
                <h2 id="3">Über 20 Bäume müssen gefällt werden</h2>
                <p>
                    Bereits in der <strong>zweiten Januarhälfte</strong> werden rund <strong>zwei Dutzend Bäume gefällt</strong>, die an den Auffahrten auf der westlichen Seite direkt neben 
                    der Fahrbahn in der Böschung stehen. Wegen der Fahrradstreifen verbreitert sich die Brücke im Vergleich zum aktuellen Zustand. Zusätzlich 
                    werden die Rampen an die neue Höhenlage der Brücke angepasst, daher können die Bäume nicht erhalten bleiben, heißt es. Die Fällarbeiten werden 
                    zeitlich vorgezogen, da diese nur außerhalb der <strong>Nist- und Brutzeiten</strong> der Vögel erlaubt sind. Für die Entfernung der Bäume seien im Bebauungsplan 
                    <strong>Ausgleichsmaßnahmen</strong> vorgesehen.
                </p>
                <h2 id="4">Sperrung ab Mitte Januar</h2>
                <p>
                    Für die Rodungsarbeiten wird die <strong>Rampe aus Richtung Eurener</strong> Straße ab <strong>Montag, 16.01.2023</strong>, ab der Einmündung Über Brücken gesperrt. 
                    Die Umleitung führt über die Rampe Hornstraße. Am 23. Januar wechseln die Arbeiten auf die Rampe aus Richtung Hornstraße, die ab der 
                    Zufahrt zum DM-Markt gesperrt wird. Die Umleitung erfolgt über die Rampe Eurener Straße. Die Arbeiten dauern voraussichtlich bis Samstag, 28. Januar.
                </p>
                <h2 id="5">Provisorische Brücke für Fußgänger</h2>
                <p>
                    Als weitere vorbereitende Maßnahme wird eine <strong>provisorische Fußgängerbrücke</strong> über die Bahngleise errichtet. 
                    Fußgänger müssten sonst auf der Strecke von der Innenstadt/Römerbrücke nach Trier-West für mehrere Jahre sehr große Umwege in Kauf nehmen. 
                    Das Provisorium wird etwa auf Höhe des DM-Markts an der Hornstraße errichtet und soll mit einem Aufzug ausgestattet werden.
                </p>
                <h2 id="6">Offizieller Baubeginn im Mai</h2>
                <p>
                    Im <strong>Mai starten</strong> die eigentlichen Bauarbeiten. Der erste Abschnitt umfasst den <strong>Abriss und Neubau</strong> der Brücke, 
                    den Aufbau der Stützwände und den Straßenbau auf der östlichen Zufahrt. Er soll bis <strong>Ende 2024</strong> zeitgleich mit dem Start des Personenverkehrs 
                    auf der Eisenbahn-Weststrecke abgeschlossen sein. Im zweiten Schritt wird bis Ende 2025 der Straßenbau auf der westlichen Seite fertiggestellt.
                </p>
